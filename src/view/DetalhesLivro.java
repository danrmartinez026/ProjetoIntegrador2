    private void fEstoqueKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_fEstoqueKeyTyped
        Character ch = evt.getKeyChar();
        String permitidos = "0123456789";
        if(!permitidos.contains(ch.toString())){
            evt.consume();
        }
        
        if(fEstoque.getText().length() == 4){
            evt.consume();
            JOptionPane.showMessageDialog(rootPane, "Maximo 4 Numeros");
        }
    }//GEN-LAST:event_fEstoqueKeyTyped

    private void buttonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonExcluirActionPerformed
        if(JOptionPane.showConfirmDialog(this, "Deseja realmente excluir este cadastro") == 0){  
            try{
                ServiceLivro.excluirlivro(livro);
                JOptionPane.showMessageDialog(rootPane, "Livro excluido");
                this.dispose();
            } catch(Exception e){
                JOptionPane.showMessageDialog(rootPane, e.getMessage(),"Erro", 
                JOptionPane.ERROR_MESSAGE);
            }
        } 
    }//GEN-LAST:event_buttonExcluirActionPerformed

